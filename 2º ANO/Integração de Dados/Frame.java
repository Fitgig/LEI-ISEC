    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try {                                         
                // TODO add your handling code here:
                String[] output = jTextField1.getText().split(",");
                String nome = output[0];
                Escritores x = Escritores.criaEscritor(nome);
                Document doc = XMLJDomFunctions.lerDocumentoXML("escritores.xml");
                try {
                    doc = ModeloXML.adicionaEscritor(x, doc);
                } catch (SaxonApiException ex) {
                    System.out.println("-----o ficheiro nao existe ----");
                    Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
                }
                jDialog1.setVisible(false);             
                if (doc != null) {
                    XMLJDomFunctions.escreverDocumentoParaFicheiro(doc, "escritores.xml");
                    JOptionPane.showMessageDialog(this,
                            "Escritor adicionado com sucesso",
                            "Informação",
                            JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(this,
                            "Escritor já existe no ficheiro",
                            "Informação",
                            JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (IOException ex) {
                JOptionPane.showMessageDialog(this,
                    "Erro a adicionar o escritor",
                    "Informação", JOptionPane.INFORMATION_MESSAGE);
                    Logger.getLogger(Frame.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_jButton1ActionPerformed

